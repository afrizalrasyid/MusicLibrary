class Product {
  final String id;
  final String name;
  final String seri;
  final String type;
  final String price;
  final String description;
  final String image;

  Product({
    required this.id,
    required this.name,
    required this.seri,
    required this.type,
    required this.price,
    required this.description,
    required this.image,
  });
}
